function count(){
	let c = '%answ%/%total%'

	let abc = c.replace('%answ%', questionIndex + 1 ).replace('%total%', QUESTIONS.length)

	counter.innerHTML = abc

}


function clearPage() {
	headerContainer.innerHTML = '';
	listContainer.innerHTML = ''
}

function showResult() {
	const resultsTemplate = `
		<h2 class="title">Ваьлар хьо</br>
		Нийс жоп денна хаттараш: %result%
		</h2> 
	`
	

	const results = resultsTemplate.replace('%result%', score)
	headerContainer.innerHTML = results;

}

function showQuestion(){

	const headerTemplate = `<h2 class="title">%template%</h2>`
	const title = headerTemplate.replace('%template%', QUESTIONS[questionIndex]['question'])
	headerContainer.innerHTML = title

	for([index, item] of QUESTIONS[questionIndex ]['answers'].entries()){

		const questionTemplate = `
			<li>
				<label>
					<input value='%num%' type="radio" class="answer" name="answer">
					<span>%answer%</span>
				</label>
			</li>
		`

		const answerQeus = questionTemplate.replace('%answer%', item).replace('%num%', index + 1)

		listContainer.innerHTML += answerQeus
	}
}

function checkAnswer() {

	const checkedReadio = listContainer.querySelector('input[type = "radio"]:checked')

	if (!checkedReadio) {
				return
	}

	const userAnswer = +checkedReadio.value

	if (userAnswer === QUESTIONS[questionIndex]['correct']) {
		score++
	}

	if (questionIndex !== QUESTIONS.length - 1) {

		questionIndex++
		clearPage()
		showQuestion()

	} else {
		clearPage()
		showResult()
	}

	count()
	submitBtn.blur();
}


